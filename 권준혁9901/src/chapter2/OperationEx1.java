package chapter2;

public class OperationEx1 {

	public static void main(String[] args) {
		int gameScore = 150;

		System.out.println(gameScore);
		System.out.println(++gameScore);
		System.out.println(gameScore++);
		System.out.println(gameScore);

		System.out.println(--gameScore);
		System.out.println(gameScore--);
		System.out.println(gameScore);

	}
}
